import galois
from collections import Counter

def genNibbleMatrix( P, A ):
    GF16 = galois.GF( 2**16 )
    PM = GF16.Zeros( (16, 16) )
    for r in range( 16 ):
        PM[r, P[r] ] = GF16(1)
    A = GF16( A )
    B = PM.dot( A )

    return B.tolist()

