// conversion from 16 entries of nibbles to uint64_t
uint64_t nibble2int(uint8_t *state, uint32_t length=16){
    uint64_t s = 0;
    for (uint32_t i = 0; i < length; i++){
        s = s << 4;
        s ^= state[i];
    }
    return s;
}

void int2nibble(uint8_t *state, uint64_t val, uint32_t length=16){
    for (uint32_t i = 0; i < length; i++){
        state[i] = (val >> (60-4*i)) & 0xf;
    }
    return;
}


