void substitution(uint8_t *state){
    for (uint32_t i = 0; i < 16; i++){
        state[i] = MANTIS_SBOX[state[i]];
    }
    return;
}

void bit_perm(uint8_t *state, uint32_t bit_perm[64]){
    uint64_t res = 0;
    uint64_t s = nibble2int(state);
    for (uint32_t i = 0; i < 16; i++){
        s = s << 4;
        s ^= state[i];
    }
    for (uint32_t i = 0; i < 64; i++){
        res ^= ((s >> (63-i)) & 1) << (63-bit_perm[i]);
    }
    int2nibble(state, res);
    return;
}

void linear(uint8_t *state, uint64_t matrix[64]){
    uint64_t res = 0;
    uint64_t s = nibble2int(state);
    for (uint32_t i = 0; i < 64; i++){
        if ((s >> (63-i)) & 1){
            res ^= matrix[i];
        }
    }
    int2nibble(state, res);
    return;
}

void add_round_key(uint8_t *state, uint8_t key[16]){
    for (uint32_t i = 0; i < 16; i++){
        state[i] = state[i] ^ key[i];
    }
    return;
}

