// key related functions
void key_permutation(uint8_t key[16]){
    uint8_t tmp[16] = {0};
    for (int i = 0; i < 16; i++) tmp[i] = key[P[i]];
    for (int i = 0; i < 16; i++) key[i] = tmp[i];
}
void key_diffusion(uint8_t key[16]){
    key[3] = key[0] ^ key[3];
    key[7] = key[4] ^ key[7];
    key[11] = key[8] ^ key[11];
    key[15] = key[12] ^ key[15];
}

void key_lfsr(uint8_t key[16]){
    for (int i = 0; i < 16; i++){
        key[i] = LFSR[key[i]];
    }
}

